    private void initTable() {
        tblModel = new DefaultTableModel();
        String[] columns = new String[]{"User name", "Password", "Role"};
        tblModel.setColumnIdentifiers(columns);
        tbl_user.setModel(tblModel);
    }

    private void fillTable() {
        while (tblModel.getRowCount() > 0) //xoa
        {
            tblModel.removeRow(0);
        }
        for (user item : list) {
            // add cac gia tri vao tblmodel
            tblModel.addRow(new String[]{item.getName(), item.getPassword(), item.getRole()});
        }
        //thay doi cac gia tri sau khi add
        tblModel.fireTableDataChanged();

    }

