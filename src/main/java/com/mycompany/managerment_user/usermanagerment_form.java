    private void tbl_userMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_userMouseClicked
        // TODO add your handling code here:

        int selected = tbl_user.getSelectedRow();
        if (selected >= 0) {
            String username = (String) tblModel.getValueAt(selected, 0);
            for (user item : list) {
                if (item.getName().equals(username)) {
                    txt_username.setText(item.getName());
                    txt_password.setText(item.getPassword());
                    if (item.getRole().equals("user")) {
                        radio_user.setSelected(true);
                    } else {
                        radio_admin.setSelected(true);
                    }
                    return;

                }
            }
        }
    }//GEN-LAST:event_tbl_userMouseClicked

