    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        // TODO add your handling code here:
        StringBuffer sb = new StringBuffer();
        if (txt_username.getText().equals("")) {
            sb.append("usernaem is empty\n");
        }
        if (txt_password.getText().equals("")) {
            sb.append("passsword is empty");
        }
        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(this, sb.toString(), "error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        user user = new user();
        user.setName(txt_username.getText());
        user.setPassword(new String(txt_password.getText()));
        user.setRole(radio_user.isSelected() ? "user" : "admin");
        list.add(user);
        fillTable();
        JOptionPane.showMessageDialog(this, "da add thanh cong");
        btn_resetActionPerformed(evt);

    }//GEN-LAST:event_btn_addActionPerformed

    private void btn_removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_removeActionPerformed
        // TODO add your handling code here:
        StringBuffer sb = new StringBuffer();
        if (txt_username.getText().equals("")) {
            sb.append("usernaem is empty\n");
        }
        if (txt_password.getText().equals("")) {
            sb.append("passsword is empty");
        }
        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(this, sb.toString(), "error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        for (user item : list) {

            if (item.getName().equals(txt_username.getText())) {
                int choice = JOptionPane.showConfirmDialog(this, "do you want remove?", "conformation", JOptionPane.YES_NO_OPTION);
                if (choice == JOptionPane.YES_OPTION) {
                    list.remove(item);
                    JOptionPane.showMessageDialog(this, "xoa thanh cong");
                    fillTable();
                    btn_resetActionPerformed(evt);
                }
                return;
            }
        }
    }//GEN-LAST:event_btn_removeActionPerformed

    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        // TODO add your handling code here:
        StringBuffer sb = new StringBuffer();
        if (txt_username.getText().equals("")) {
            sb.append("usernaem is empty\n");
        }
        if (txt_password.getText().equals("")) {
            sb.append("passsword is empty");
        }
        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(this, sb.toString(), "error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        for (user item : list) {
            if (item.getName().equals(txt_username.getText())) {
                item.setPassword(new String(txt_password.getPassword()));
                item.setRole(radio_user.isSelected() ? "user" : "admin");
                JOptionPane.showMessageDialog(this, "update thanh cong ");
                fillTable();
                btn_resetActionPerformed(evt);
                return;
            }
        }
        JOptionPane.showMessageDialog(this, "khong the update ");
    }//GEN-LAST:event_btn_updateActionPerformed

